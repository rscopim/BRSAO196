# Cria uma lista chamada "listaMista" com diferentes tipos de dados
listaMista = [45, 290578, 1.02, True, "Meu cachorro está na cama", "45"]
# Inicia um loop "for" que percorre cada elemento da lista
for list in listaMista:
    # Imprime valor de cada elemento e o tipo de dado
    print("{} é do tipo de dado {}" .format(list, type(list))) # .format está formatando as "{} chaves" com o elemento e valor correto.
