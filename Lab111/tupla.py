# Cria uma tupla chamada 'fruta' com três elementos: "maça", "banana" e "laranja"
fruta = ("maça", "banana", "laranja")
# Imprime toda a tupla
print(fruta)  # Saída: ('maça', 'banana', 'laranja')
# Mostra o tipo de dado da variável 'fruta'
print(type(fruta))  # Saída: <class 'tuple'>, ou seja, 'fruta' é uma tupla
# Acessa e imprime o primeiro elemento da tupla (posição 0)
print(fruta[0])  # Saída: maça
# Acessa e imprime o segundo elemento da tupla (posição 1)
print(fruta[1])  # Saída: banana
# Acessa e imprime o terceiro elemento da tupla (posição 2)
print(fruta[2])  # Saída: laranja
