# listas - que permite alterar os elementos
frutas = ["maçã", "banana", "laranja"] # Coleção ordenada
print(frutas [1]) 

# Tupla - não permite alterar os elementos
fruta = ("maçã", "banana", "laranja")
print(fruta[1])

# Dicionário - coleção de chave:valor
aluno = {"nome": "Guilherme", 
         "idade": "100"}
print(aluno["nome"])

# Conjunto = coleção sem ordem e sem elementos repetidos
numeros = {1, 2, 3, 4, 3, 1, 2}
print(numeros)

